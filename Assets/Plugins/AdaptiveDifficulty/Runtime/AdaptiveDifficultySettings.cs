using UnityEngine;

namespace AdaptiveDifficulty.Runtime
{
    /// <summary>
    /// Настройки системы адаптивной сложности.
    /// Создать: ПКМ → AdaptiveDifficulty → New AdaptiveDifficultySettings
    /// </summary>
    [CreateAssetMenu(fileName = nameof(AdaptiveDifficultySettings), menuName = "AdaptiveDifficulty/New AdaptiveDifficultySettings")]
    public sealed class AdaptiveDifficultySettings : ScriptableObject
    {
        // ─────────────────────────────────────────────────────────────────────
        // СГЛАЖИВАНИЕ МЕТРИК (EMA)
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Сглаживание метрик (EMA) ━━━")]
        [Tooltip("Временная константа τ (сек) для EMA убийств.\n" +
                 "Чем больше τ, тем медленнее значение реагирует на изменения.\n" +
                 "τ=8 → ~8 сек «памяти» — резкие всплески убийств нивелируются.\n" +
                 "Рекомендуется: 5–12 сек.")]
        [Min(0.01f)] public float TauKills = 8f;

        [Tooltip("Временная константа τ (сек) для EMA нанесённого урона.\n" +
                 "Большое τ → медленная реакция, устойчивая оценка.\n" +
                 "Рекомендуется: 5–12 сек.")]
        [Min(0.01f)] public float TauDamageDealt = 8f;

        [Tooltip("Временная константа τ (сек) для EMA полученного урона.\n" +
                 "Немного больше TauDamageDealt, чтобы перегрузка сглаживалась плавнее.\n" +
                 "Рекомендуется: 8–15 сек.")]
        [Min(0.01f)] public float TauDamageTaken = 10f;

        [Tooltip("Временная константа τ (сек) для EMA точности стрельбы.\n" +
                 "Меньше τ → точность реагирует быстрее на промахи/попадания.\n" +
                 "Рекомендуется: 4–8 сек.")]
        [Min(0.01f)] public float TauAccuracy = 6f;

        [Tooltip("Временная константа τ (сек) для EMA длины комбо.\n" +
                 "Комбо — краткосрочная метрика, поэтому τ меньше остальных.\n" +
                 "Рекомендуется: 2–6 сек.")]
        [Min(0.01f)] public float TauCombo = 4f;

        [Tooltip("Временная константа τ (сек) для EMA использования расходников.\n" +
                 "Большое τ → частый подбор лечилок/патронов оценивается устойчиво.\n" +
                 "Рекомендуется: 10–20 сек.")]
        [Min(0.01f)] public float TauPickups = 12f;

        // ─────────────────────────────────────────────────────────────────────
        // НОРМАЛИЗАЦИЯ МЕТРИК
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Нормализация метрик ━━━")]
        [Tooltip("Количество убийств в секунду, соответствующее значению 1.0 (100%).\n" +
                 "Подберите под среднее количество врагов на уровне.\n" +
                 "Пример: 0.33 = 1 убийство каждые ~3 сек → максимальный вклад в I.\n" +
                 "Рекомендуется: 0.15–0.5 уб/сек.")]
        [Min(0.001f)] public float NormalizedKillsPerSecond = 0.33f;

        [Tooltip("Урон, нанесённый в секунду, соответствующий значению 1.0 (100%).\n" +
                 "Зависит от DPS оружия игрока и HP врагов в проекте.\n" +
                 "Рекомендуется: 20–100 ед/сек.")]
        [Min(0.001f)] public float NormalizedDamageDealtPerSecond = 50f;

        [Tooltip("Урон, полученный в секунду, соответствующий значению 1.0 (100%) уязвимости.\n" +
                 "Базируйтесь на уроне врагов. Если враг бьёт на 20 DPS и игрок выдержит ~5 сек → 25/сек.\n" +
                 "Рекомендуется: 10–50 ед/сек.")]
        [Min(0.001f)] public float NormalizedDamageTakenPerSecond = 25f;

        [Tooltip("Длина комбо, соответствующая значению 1.0 (100%) вклада комбо в I.\n" +
                 "Пример: 10 убийств подряд без получения урона = максимальный комбо-вклад.\n" +
                 "Рекомендуется: 5–15.")]
        [Min(0.001f)] public float NormalizedCombo = 10f;

        [Tooltip("Количество подборов в секунду, соответствующее значению 1.0 (100%) нагрузки от расходников.\n" +
                 "Пример: 0.5 = каждые 2 сек подбирается аптечка/патрон.\n" +
                 "Рекомендуется: 0.1–1.0 шт/сек.")]
        [Min(0.001f)] public float NormalizedPickupsPerSecond = 0.5f;

        // ─────────────────────────────────────────────────────────────────────
        // ПОКАЗАТЕЛЬ ЭФФЕКТИВНОСТИ (I)
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Показатель эффективности I ━━━")]
        [Tooltip("Вес убийств в расчёте I.\n" +
                 "Формула: I = KillWeight·kills + ComboWeight·combo + DamageWeight·damage\n" +
                 "Высокое значение → убийства сильно влияют на рост сложности.\n" +
                 "Рекомендуется: 0.3–0.5. Сумма весов не обязана = 1, но рекомендуется.")]
        [Range(0f, 1f)] public float KillWeight = 0.45f;

        [Tooltip("Вес длины комбо в расчёте I.\n" +
                 "Комбо-убийства (без получения урона) агрессивно повышают I → сложность растёт.\n" +
                 "Рекомендуется: 0.2–0.4.")]
        [Range(0f, 1f)] public float ComboWeight = 0.30f;

        [Tooltip("Вес нанесённого урона в расчёте I.\n" +
                 "Активное ведение огня повышает I независимо от точности.\n" +
                 "Рекомендуется: 0.15–0.35.")]
        [Range(0f, 1f)] public float DamageWeight = 0.25f;

        [Tooltip("Гамма-коррекция показателя I.\n" +
                 "Γ > 1 → система требует высокого I для заметного роста сложности (менее отзывчива к слабым игрокам).\n" +
                 "Γ = 1 → линейная зависимость.\n" +
                 "Γ < 1 → сложность растёт быстрее при среднем I.\n" +
                 "Рекомендуется: 1.0–1.5.")]
        [Min(0.01f)] public float PerformanceGamma = 1.3f;

        // ─────────────────────────────────────────────────────────────────────
        // ПОКАЗАТЕЛЬ БЕЗОПАСНОСТИ (S_safety)
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Показатель уязвимости S_safety ━━━")]
        [Tooltip("Вес получаемого урона в расчёте S_safety.\n" +
                 "Формула: S = DamageTakenWeight·dmgTaken + AccuracyPenaltyWeight·(1-acc) + PickupsWeight·pickups\n" +
                 "Высокое значение → большой получаемый урон сильно снижает сложность.\n" +
                 "Рекомендуется: 0.4–0.6.")]
        [Range(0f, 1f)] public float DamageTakenWeight = 0.50f;

        [Tooltip("Вес штрафа за низкую точность в S_safety.\n" +
                 "Штраф = 1 − NormalizedAccuracy. При идеальной точности = 0, при 0% точности = 1.\n" +
                 "Высокое значение → промахи сильно защищают игрока от повышения сложности.\n" +
                 "Рекомендуется: 0.2–0.4.")]
        [Range(0f, 1f)] public float AccuracyPenaltyWeight = 0.30f;

        [Tooltip("Вес использования расходников (аптечки, патроны) в S_safety.\n" +
                 "Частый подбор хилок = признак перегрузки.\n" +
                 "Рекомендуется: 0.1–0.25.")]
        [Range(0f, 1f)] public float PickupsWeight = 0.20f;

        // ─────────────────────────────────────────────────────────────────────
        // ОБНОВЛЕНИЕ СЛОЖНОСТИ (D)
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Обновление сложности D ━━━")]
        [Tooltip("Начальное значение сложности D при старте игры (до первого уровня).\n" +
                 "0 = минимально лёгкая, 1 = максимально сложная.\n" +
                 "Рекомендуется: 0.3–0.5 для среднего старта.")]
        [Range(0f, 1f)] public float InitialDifficulty = 0.40f;

        [Tooltip("Скорость адаптации β при РОСТЕ сложности (I > S_safety).\n" +
                 "Формула при росте: D_new = D + BetaUp × (I − S_safety)\n" +
                 "Меньше BetaUp → сложность растёт плавнее.\n" +
                 "Рекомендуется: 0.05–0.15.")]
        [Min(0f)] public float BetaUp = 0.10f;

        [Tooltip("Скорость адаптации β при СНИЖЕНИИ сложности (I < S_safety).\n" +
                 "Формула при снижении: D_new = D + BetaDown × (I − S_safety)\n" +
                 "BetaDown чуть больше BetaUp → снижение сложности при перегрузке происходит быстрее (защита игрока).\n" +
                 "Рекомендуется: 0.08–0.18.")]
        [Min(0f)] public float BetaDown = 0.12f;

        [Tooltip("Максимальное изменение D за один вызов CommitLevel (один уровень).\n" +
                 "Ограничивает резкие скачки сложности между уровнями.\n" +
                 "Пример: 0.15 → за один уровень D не может измениться более чем на 15%.\n" +
                 "Рекомендуется: 0.10–0.20.")]
        [Range(0f, 1f)] public float MaxDifficultyDelta = 0.15f;

        // ─────────────────────────────────────────────────────────────────────
        // ИНТЕНСИВНОСТЬ В РЕАЛЬНОМ ВРЕМЕНИ (для саундтрека)
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Интенсивность в реальном времени (саундтрек) ━━━")]
        [Tooltip("Временная константа τ (сек) нарастания RuntimeIntensity.\n" +
                 "RuntimeIntensity — быстрая оценка активности игрока (обновляется каждый кадр).\n" +
                 "Малое τ → интенсивность нарастает быстро (агрессивная реакция саундтрека).\n" +
                 "Рекомендуется: 1.5–4 сек.")]
        [Min(0.01f)] public float RuntimeTau = 2.5f;

        [Tooltip("Временная константа τ (сек) сглаживания AudioIntensity.\n" +
                 "AudioIntensity — финальное значение, управляющее слоями музыки.\n" +
                 "AudioTau < RuntimeTau → музыка реагирует ещё плавнее, без резких переключений.\n" +
                 "Рекомендуется: 0.3–1.5 сек.")]
        [Min(0.01f)] public float AudioTau = 0.6f;

        // ─────────────────────────────────────────────────────────────────────
        // КОМБО
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Комбо ━━━")]
        [Tooltip("Пауза (сек) без убийства, после которой текущее комбо сбрасывается в 0.\n" +
                 "Пример: 3 сек → если игрок 3 секунды не убивает врагов, комбо обнуляется.\n" +
                 "Рекомендуется: 2–5 сек.")]
        [Min(0.01f)] public float ComboResetDelay = 3f;

        // ─────────────────────────────────────────────────────────────────────
        // ИНТЕГРАЦИЯ С ПРОЕКТОМ
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Интеграция с проектом ━━━")]
        [Tooltip("Максимальное HP игрока по умолчанию.\n" +
                 "Используется если в ECS-компоненте Health не задан MaxValue.\n" +
                 "Рекомендуется: задавать в PlayerStaticData, здесь оставить как fallback.")]
        [Min(0)] public int DefaultPlayerMaxHealth = 100;

        [Tooltip("Базовая броня игрока по умолчанию.\n" +
                 "Используется если в ECS-компоненте Armor не задано значение.")]
        [Min(0)] public int DefaultPlayerArmor = 0;

        [Tooltip("Базовый урон снаряда по умолчанию.\n" +
                 "Используется если в ECS-компоненте ProjectileDamage не задано значение.")]
        [Min(1)] public int DefaultProjectileDamage = 10;

        [Tooltip("Радиус поиска владельца снаряда при коллизии (физический overlap, в Units).\n" +
                 "Если снаряд не имеет владельца в компоненте — система ищет ближайший EcsEntity.\n" +
                 "Рекомендуется: 0.5–2.0.")]
        [Min(0.01f)] public float ProjectileOwnerSearchRadius = 1f;

        [Tooltip("Если включено: любое уменьшение HP врага засчитывается как урон игрока.\n" +
                 "Оставьте ВЫКЛЮЧЕНО для текущего проекта, где урон по врагам отслеживается через ProjectileCollisionSystem.\n" +
                 "Включайте только если нет корректной связи снаряд→команда.")]
        public bool CountAnyEnemyHealthLossAsPlayerDamage = false;

        [Tooltip("Если включено: удаление врага через DeathRequest считается убийством игрока.\n" +
                 "Оставьте ВКЛЮЧЕНО — EnemyDeathSystem вызывает ReportEnemyKilled() при смерти.")]
        public bool CountEnemyDespawnAsKill = true;

        // ─────────────────────────────────────────────────────────────────────
        // ОТЛАДКА
        // ─────────────────────────────────────────────────────────────────────

        [Header("━━━ Отладка ━━━")]
        [Tooltip("Включить вывод текущих показателей (D, I, S, Intensity) в Unity Console.\n" +
                 "Вывод происходит с интервалом DebugLogInterval сек.\n" +
                 "Рекомендуется: выключить в релизе.")]
        public bool EnableDebugLogs = true;

        [Tooltip("Интервал (сек) вывода отладочных логов в консоль.\n" +
                 "Чем меньше — тем чаще появляются логи.\n" +
                 "Рекомендуется: 2–5 сек.")]
        [Min(0.1f)] public float DebugLogInterval = 2f;

        [Tooltip("Показывать дебаг-оверлей с текущими метриками системы сложности в Play Mode.\n" +
                 "Переключить в игре: клавиша F1 (по умолчанию).\n" +
                 "Рекомендуется: выключить в релизе.")]
        public bool ShowDebugHud = true;
    }
}
